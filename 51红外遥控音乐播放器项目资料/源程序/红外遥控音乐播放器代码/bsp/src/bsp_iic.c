#include "bsp.h"
sbit  SDA = P2^1;
sbit  SCL = P2^0;
/*
*********************************************************************************************************
*	函 数 名: i2c_Delay
*	功能说明: I2C总线位延迟，最快400KHz
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
static void i2c_Delay(void)
{
	unsigned char i;

	/*　
	    IIC 延时时间根据具体情况自行决定for循环延迟时间的大小

		实际应用选择小于400KHz左右的速率即可
	*/
	for (i = 0; i < 20; i++);
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Start
*	功能说明: CPU发起I2C总线启动信号
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_Start(void)
{
	/* 当SCL高电平时，SDA出现一个下跳沿表示I2C总线启动信号 */
	SDA = 1;
	SCL = 1; //空闲状态
	i2c_Delay();
	//下降沿
	SDA = 0;
    i2c_Delay();


	SCL =0; //可以允许数据 发生变化	SCL =1 数据不能变  SCL =0  数据可以变

	i2c_Delay();
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Start
*	功能说明: CPU发起I2C总线停止信号
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_Stop(void)
{
	/* 当SCL高电平时，SDA出现一个上跳沿表示I2C总线停止信号 */
	SDA = 0;
	SCL = 1;
    i2c_Delay();
	SDA = 1;//上升沿

	i2c_Delay();

	//SCL =0;  ?
	
}

/*
*********************************************************************************************************
*	函 数 名: i2c_SendByte
*	功能说明: CPU向I2C总线设备发送8bit数据
*	形    参:  _ucByte ： 等待发送的字节
*	返 回 值: 无
*  iic 先发最高位 ---》最低位
*  11001010
*  如果已经start了 这个时候 scl =0
*********************************************************************************************************
*/
void i2c_SendByte(unsigned char _ucByte)
{
	unsigned char i;
	for(i=0;i<8;i++)
	{
	 	if(_ucByte &0x80)  //需要发送的变量最高位=1
		{
		    SDA = 1;
		}
		else
		    SDA = 0;
		 SCL = 1;
	    //数据从这里开始不允许变 
		i2c_Delay();
		SCL = 0;
		_ucByte<<=1;
		if(i==7)//如果是最后一位 
		{
		   SDA = 1;//释放总线--将数据线拉高
		}
		i2c_Delay();
	}
}

/*
*********************************************************************************************************
*	函 数 名: i2c_ReadByte
*	功能说明: CPU从I2C总线设备读取8bit数据
*	形    参:  无
*	返 回 值: 读到的数据
*   先收到最高位 -先存在最低位
*********************************************************************************************************
*/
unsigned char i2c_ReadByte(void)
{
	unsigned char i;
	unsigned char value=0;
	for(i=0;i<8;i++)
	{
		value <<=1;
	    SCL = 1;
		i2c_Delay();
		if(SDA ==1)
		 	value++;
		SCL =0;
		i2c_Delay();
	
	}
	return  value;
}

/*
*********************************************************************************************************
*	函 数 名: i2c_WaitAck
*	功能说明: CPU产生一个时钟，并读取器件的ACK应答信号
*	形    参:  无
*	返 回 值: 返回0表示正确应答，1表示无器件响应
*********************************************************************************************************
*/
unsigned char i2c_WaitAck(void)
{
	unsigned char re;
	SDA = 1;//释放总线 ，对方才能拉低 应答
	i2c_Delay();
	SCL = 1;
	i2c_Delay();
	if(SDA ==1)
	  re = 1;
	else
	  re = 0;
	SCL = 0;
    i2c_Delay();
	return re;

}

/*
*********************************************************************************************************
*	函 数 名: i2c_Ack
*	功能说明: CPU产生一个ACK信号
*	形    参:  无
*	返 回 值: 无
*   对SDA 进行操作的 尽量把SDA的操作放前面 不要吧scl 放前面
*********************************************************************************************************
*/
void i2c_Ack(void)
{
	SDA = 0;
	i2c_Delay();
	SCL = 1;
	i2c_Delay();
	SCL = 0;
	i2c_Delay();
	
	SDA  = 1;  //释放总线
	i2c_Delay();		
}

/*
*********************************************************************************************************
*	函 数 名: i2c_NAck
*	功能说明: CPU产生1个NACK信号
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_NAck(void)
{
 	SDA  =1;
	i2c_Delay();
	SCL = 1;
	i2c_Delay();
	SCL = 0;
	i2c_Delay();
}

/*
*********************************************************************************************************
*	函 数 名: i2c_CheckDevice
*	功能说明: 检测I2C总线设备，CPU向发送设备地址，然后读取设备应答来判断该设备是否存在
*	形    参:  _Address：设备的I2C总线地址
*	返 回 值: 返回值 0 表示正确， 返回1表示未探测到
*********************************************************************************************************
*/
unsigned char i2c_CheckDevice(unsigned char _Address)
{
	unsigned char ucAck;

	if (SDA == 1 && SCL == 1)  //iic 总线处于空闲
	{
		i2c_Start();		/* 发送启动信号 */

		/* 发送设备地址+读写控制bit（0 = w， 1 = r) bit7 先传 */
		i2c_SendByte(_Address | I2C_WR);
		ucAck = i2c_WaitAck();	/* 检测设备的ACK应答 */

		i2c_Stop();			/* 发送停止信号 */

		return ucAck;
	}
	return 1;	/* I2C总线异常 */
}
